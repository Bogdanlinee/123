$(document).ready(function () {
	$('.header-two__burger').click(function (event) {
		$('.header-two__burger,.header-two__left-menu,.header-two__right-menu').toggleClass('active');
	});
	$('.header-two__burger').click(function (event) {
		$('body').toggleClass('lock');
	});
});
